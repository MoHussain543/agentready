// Bakes AGENTREADY_TOKEN into src/constants.ts at build time.
// Set the env var before running `npm run build` for release builds.
// Dev builds without the token will have an empty string — Pro review is skipped silently.
import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { join, dirname } from "path";

const token = process.env.AGENTREADY_TOKEN ?? "";
const out = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "constants.ts");
writeFileSync(out, `// Auto-generated by scripts/bake-constants.mjs — do not edit.\nexport const BAKED_APP_TOKEN = ${JSON.stringify(token)};\n`);
